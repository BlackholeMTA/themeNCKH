                                        <p class="post-title-main">
                                            About LQD-JST
                                        </p>
                                        <p class="post-content">
                                            LQD-JST is a special edition of the Journal of Science and Technology (JST), which has been included in the list of journals (endorsed by the State Professor Title Council of Science and Technology), and published by Le Quy Don Technical University (a.k.a. Le Quy Don University) publishing house. The journal is a scholarly forum for scientists in all areas of Information Technology and Communications, publishing quality articles. The articles are thoroughly peer-reviewed ensuring the new and novel results.
                                        </p>
                                        <p class="post-content">
                                            All articles will be reviewed by the editors and two reviewers. In some cases, the advice of a third reviewer will be sought as warranted. The decision of the editors and the referees' reviews will be returned to the authors by email. The normal timeframe from acknowledgement of receipt to notification of decision is three months
                                        </p>
                                        

